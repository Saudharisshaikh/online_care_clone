package com.app.msu_cp.careplan;

public class DrawerItemBean {

	private String itemName;
	private int iconId;
	public DrawerItemBean(String itemName, int iconId) {
		super();
		this.itemName = itemName;
		this.iconId = iconId;
	}
	public String getItemName() {
		return itemName;
	}
	public int getIconId() {
		return iconId;
	}
	
	
	
}
